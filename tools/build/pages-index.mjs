#!/usr/bin/env node
// Regenerate pages/README.md — the catalog of everything under pages/.
//
//   node tools/build/pages-index.mjs        -> writes pages/README.md
//   node tools/build/pages-index.mjs --check -> exit 1 if stale (CI-friendly)
//
// This is a *catalog* generator, not part of the load -> build -> bake -> export
// code pipeline (tools/README.md). It resolves no dependency graph; it walks the
// pages/ tree, reads each page's <title>, and emits a dense markdown table giving
// two links per page: the rendered github.io URL and the github.com source view.
// GitHub auto-renders pages/README.md in the folder listing, so the catalog shows
// up exactly where you'd browse for a page.
//
// Run it whenever pages are added, removed, or retitled.

import { readFile, writeFile, readdir } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO = 'mehrlander/web-tools';
const PAGES_URL = `https://mehrlander.github.io/${REPO.split('/')[1]}/pages`;
const SRC_URL = `https://github.com/${REPO}/blob/main/pages`;

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..', '..');
const pagesDir = path.join(repoRoot, 'pages');
const outPath = path.join(pagesDir, 'README.md');

// Recursively collect every .html under pages/, as paths relative to pages/.
async function walk(dir) {
  const out = [];
  for (const ent of await readdir(dir, { withFileTypes: true })) {
    const abs = path.join(dir, ent.name);
    if (ent.isDirectory()) out.push(...await walk(abs));
    else if (ent.name.endsWith('.html')) out.push(path.relative(pagesDir, abs));
  }
  return out;
}

const titleOf = src => (src.match(/<title>([^<]*)<\/title>/i)?.[1] ?? '').trim();

const pages = (await walk(pagesDir)).sort((a, b) => a.localeCompare(b));

// Group by the directory the page sits in (relative to pages/), so the catalog
// mirrors the folder tree. '' is the pages/ root group, listed first.
const groups = new Map();
for (const rel of pages) {
  const dir = path.dirname(rel) === '.' ? '' : path.dirname(rel);
  const src = await readFile(path.join(pagesDir, rel), 'utf8');
  (groups.get(dir) ?? groups.set(dir, []).get(dir)).push({
    rel,
    name: path.basename(rel, '.html'),
    title: titleOf(src),
  });
}

const groupKeys = [...groups.keys()].sort((a, b) =>
  a === '' ? -1 : b === '' ? 1 : a.localeCompare(b));

const lines = [
  '<!-- Generated by tools/build/pages-index.mjs — do not edit by hand. -->',
  '<!-- Regenerate: npm run pages-index -->',
  '',
  '# pages/',
  '',
  `Catalog of every page under \`pages/\`. Each row links the **rendered** page`,
  `([${PAGES_URL.replace(/^https:\/\//, '')}](${PAGES_URL}/)) and its **code** on github.com.`,
  '',
  `The live, auto-fetched directory is [pages/index.html](${PAGES_URL}/); this file`,
  `is its static, link-dense twin, regenerated by \`npm run pages-index\`.`,
  '',
];

for (const key of groupKeys) {
  const rows = groups.get(key).sort((a, b) => a.name.localeCompare(b.name));
  lines.push(`## ${key === '' ? 'pages/' : `pages/${key}/`}`, '');
  lines.push('| Page | Title | Links |', '|---|---|---|');
  for (const { rel, name, title } of rows) {
    const rendered = `${PAGES_URL}/${rel}`;
    const code = `${SRC_URL}/${rel}`;
    lines.push(`| \`${name}\` | ${title || '—'} | [view](${rendered}) · [code](${code}) |`);
  }
  lines.push('');
}

const md = lines.join('\n');

if (process.argv.includes('--check')) {
  const cur = await readFile(outPath, 'utf8').catch(() => '');
  if (cur !== md) {
    console.error('pages-index: pages/README.md is stale — run `npm run pages-index`.');
    process.exit(1);
  }
  console.log('pages-index: pages/README.md is up to date.');
} else {
  await writeFile(outPath, md);
  console.log(`pages-index: wrote ${path.relative(repoRoot, outPath)} (${pages.length} pages, ${groupKeys.length} groups).`);
}
