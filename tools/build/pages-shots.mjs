#!/usr/bin/env node
// Regenerate pages/thumbs/*.png — one screenshot per page for the visual index.
//
//   node tools/build/pages-shots.mjs            -> shoots every page
//   node tools/build/pages-shots.mjs <page ...> -> shoots only the named pages
//
// Each thumbnail is the page rendered headlessly by tools/render/screenshot.mjs
// (Chromium, with CDN/own-code requests resolved to the local tree), captured at a
// uniform 16:10 viewport so the cards in pages/index.html line up. Output mirrors
// the pages/ tree under pages/thumbs/ (e.g. pages/foo.html -> pages/thumbs/foo.png).
//
// These PNGs are committed: pages/index.html (generated by pages-index.mjs) shows
// them as each card's default preview, with a live-iframe / source toggle on top.
// Re-run when a page is added or its look changes; then `npm run pages-index`.

import { readdir, mkdir } from 'node:fs/promises';
import { spawnSync } from 'node:child_process';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const WIDTH = 1000, HEIGHT = 625;            // 16:10, matches the card aspect ratio

const here = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(here, '..', '..');
const shotTool = path.join(here, '..', 'render', 'screenshot.mjs');

// Source roots, matching pages-index.mjs: pages/ itself plus the kit demos that
// live under lib/kits/demos/. `virt` is the thumbnail path prefix (so kit demos
// land in pages/thumbs/kit-demos/); `src` resolves the file to actually shoot.
const SOURCES = [
  { dir: 'pages',           virt: '',          src: rel => path.join('pages', rel) },
  { dir: 'lib/kits/demos',  virt: 'kit-demos', src: rel => path.join('lib/kits/demos', rel) },
];

async function walk(baseDir, dir = baseDir) {
  const out = [];
  for (const ent of await readdir(dir, { withFileTypes: true })) {
    if (ent.name === 'thumbs') continue;                       // never shoot the output dir
    const abs = path.join(dir, ent.name);
    if (ent.isDirectory()) out.push(...await walk(baseDir, abs));
    else if (ent.name.endsWith('.html') && ent.name !== 'index.html') out.push(path.relative(baseDir, abs));
  }
  return out;
}

// Collect { rel (virtual thumb key), src (file to shoot) } across every source.
let pages = [];
for (const s of SOURCES) {
  const files = await walk(path.join(repoRoot, s.dir));
  for (const f of files) pages.push({ rel: s.virt ? `${s.virt}/${f}` : f, src: s.src(f) });
}
pages.sort((a, b) => a.rel.localeCompare(b.rel));

const want = process.argv.slice(2).map(p => p.replace(/^pages\//, ''));
if (want.length) pages = pages.filter(p => want.includes(p.rel));

const results = [];
for (const { rel, src } of pages) {
  const out = path.join('pages', 'thumbs', rel.replace(/\.html$/, '.png'));
  await mkdir(path.dirname(path.join(repoRoot, out)), { recursive: true });
  process.stdout.write(`shooting ${rel} -> ${out} ... `);
  const r = spawnSync(process.execPath, [
    shotTool, src,
    '--out', out, '--width', String(WIDTH), '--height', String(HEIGHT),
  ], { cwd: repoRoot, encoding: 'utf8' });
  const ok = r.status === 0;
  // Surface any [pageerror]/[fatal] lines so broken pages are visible, not silent.
  const flags = (r.stdout || '').split('\n').filter(l => /\[(pageerror|fatal|requestfailed)\]/.test(l));
  console.log(ok ? 'ok' : `FAILED (exit ${r.status})`);
  results.push({ rel, ok, flags });
}

console.log(`\n=== pages-shots: ${results.filter(r => r.ok).length}/${results.length} ok ===`);
for (const r of results) {
  if (!r.ok) console.log(`  FAILED  ${r.rel}`);
  else if (r.flags.length) console.log(`  notes   ${r.rel}: ${r.flags.length} render warning(s)`);
}
process.exit(results.every(r => r.ok) ? 0 : 1);
