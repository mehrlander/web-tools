#!/usr/bin/env node
// Build a self-resolving offline bundle of a page's gh.load chain.
//
//   node tools/bundle.mjs <page-path>           -> dist/<page>.js
//
// What it emits is NOT a rewritten graph: it's the real gh-api.js loader with
// GH.prototype.get overridden by an inlined cache of every own-repo file the
// page reaches (tools/lib/graph.mjs computes that set). So the actual loader
// runs — same strip+wrap+execute, same gh-boot registry/attribution, same
// read() tracking — only the source bytes come from memory instead of the
// GitHub API. Third-party libs (Tailwind/daisyUI/Phosphor/Alpine) are untouched;
// they stay on the page's CDN <script>/<link> tags.
//
// A page adopts it by swapping just its loader import:
//     await import(`https://cdn.jsdelivr.net/gh/.../lib/gh-api.js`)
//   → await import('../dist/<page>.js')
// Everything after that (the page's gh.load('kits/...') calls) resolves from the
// cache, offline. Leaving the jsDelivr import in place is the unchanged
// gh-for-review path; the bundle is the offline/production twin.
//
// dist/ is generated (gitignored); rebuild on demand.

import { readFile, mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { buildGraph } from './lib/graph.mjs';

const REPO = 'mehrlander/web-tools';
const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');

const arg = process.argv[2];
if (!arg) {
  console.error('Usage: node tools/bundle.mjs <page-path>');
  process.exit(2);
}

// gh-api.js minus its jsDelivr auto-bootstrap tail and the `export default`,
// leaving the console-capture IIFE and the GH class as a plain declaration.
async function loaderSource() {
  let src = await readFile(path.join(repoRoot, 'lib/gh-api.js'), 'utf8');
  const cut = src.indexOf('// Auto-bootstrap when loaded');
  if (cut > 0) src = src.slice(0, cut).trimEnd() + '\n';
  src = src.replace(/export\s+default\s+class\s+GH/, 'class GH');
  return src;
}

let graph;
try {
  graph = buildGraph(repoRoot, arg);
} catch (e) {
  console.error(`bundle: ${e.message}`);
  process.exit(1);
}
const loader = await loaderSource();

const cacheEntries = graph.files
  .map(f => `  ${JSON.stringify(f.key)}: ${JSON.stringify(f.src)},`)
  .join('\n');

const baseName = path.basename(arg, path.extname(arg));
const built = new Date().toISOString().slice(0, 10);

const out = `// dist/${baseName}.js — generated by tools/bundle.mjs on ${built}. Do not edit.
// Offline form of ${arg}'s gh.load chain: the gh-api.js loader plus an inlined
// cache of every own-repo file the page loads, so the whole chain runs with no
// network. Third-party libs stay on their CDN tags. Rebuild: node tools/bundle.mjs ${arg}

${loader}
const __REPO = ${JSON.stringify(REPO)};
const __DEFAULT_REF = ${JSON.stringify(graph.defaultRef)};

// path -> source for every own-repo file ${arg} can reach. Keyed by what
// GH.prototype.get receives (loadBase 'lib/' + the gh.load path).
const __CACHE = {
${cacheEntries}
};

// Serve cached own-code from memory; anything else (cross-repo, dynamic, or a
// path the build didn't see) falls through to the real network get().
const __get = GH.prototype.get;
GH.prototype.get = async function (p) {
  if (Object.prototype.hasOwnProperty.call(__CACHE, p)) {
    const text = __CACHE[p];
    return { text, sha: 'bundle:' + __DEFAULT_REF, size: text.length, url: '' };
  }
  return __get.call(this, p);
};

// Reproduce gh-api.js's bootstrap, offline: honor ?use=<ref> (so a bundled page
// can still be re-pinned), expose window.gh, and run the gh-boot chain from cache.
if (typeof window !== 'undefined') {
  const ref = new URLSearchParams(location.search).get('use') || __DEFAULT_REF;
  window.GH = GH;
  window.gh = new GH({ repo: __REPO, ref, loadBase: 'lib/' });
  window.__bundleRef = ref;
  window.__bundledOffline = true;
  await window.gh.load('gh-boot.js');
}

export default GH;
`;

const distDir = path.join(repoRoot, 'dist');
await mkdir(distDir, { recursive: true });
const outPath = path.join(distDir, `${baseName}.js`);
await writeFile(outPath, out);

const kb = (Buffer.byteLength(out) / 1024).toFixed(0);
console.log(`bundle: ${arg}`);
console.log(`  seeds (${graph.seeds.length}): ${graph.seeds.join(', ')}`);
console.log(`  cached own-code files (${graph.files.length}):`);
for (const f of graph.files.sort((a, b) => a.rel.localeCompare(b.rel))) {
  console.log(`    ${f.rel}  (${(f.src.length / 1024).toFixed(1)}k)`);
}
if (graph.misses.length) console.log(`  misses (not on disk): ${graph.misses.join(', ')}`);
console.log(`  -> ${path.relative(repoRoot, outPath)}  (${kb}k)`);
